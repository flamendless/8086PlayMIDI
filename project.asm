MAIN PROC PROJECT

	CALL CLEAR_SCREEN ;to clear the screen
	CALL PRINT_NEWLINE ;print new line (\n)

	;Print banner (TOP)
	MOV dx, OFFSET str_banner
	CALL PRINT
	CALL PRINT_NEWLINE

	;Print colored title
	MOV ah, 09h ;Start of when changing color
	MOV bl, color_title ;set the color
	MOV cx, count_title ;set how many characters of the string to colorize
	INT 10h ;do the colored string
	MOV dx, OFFSET str_title
	CALL PRINT ;Print the title string

	;Print banner (BOTTOM)
	MOV dx, OFFSET str_banner
	CALL PRINT
	CALL PRINT_NEWLINE
	CALL PRINT_NEWLINE

	MAIN_P: ;main label (entry point of program)
		;Print song
		MOV ah, 09h
		MOV bl, color_song
		MOV cx, count_song
		INT 10h
		MOV dx, OFFSET str_song
		CALL PRINT

		CALL PRINT_NEWLINE

	PLAY_SONG: ;label to play song
		CALL PRINT_NEWLINE
		CALL PRINT_NEWLINE
		CALL START_CLOCK ;start the timer
		MOV si, 0 ;reset count to 0

		NEXT_NOTE:
			MOV bh, 0
			MOV bl, [music_song + si] ;get the current note using offset
			MOV ah, 0eh
			MOV al, bl
			INT 10h
			CMP bl, 255 ; CMP - compare
			JZ IGNORE ;JZ - Jump if Zero
			CMP bl, 254
			JZ _NOTE_OFF

		PLAY_MIDI_NOTE:
			SHL bx, 1 ;SHL - Shift Logical Left - shift the bits to the left
			MOV ax, [frequency_table + bx] ;look at the frequency_table for the note
			CALL NOTE_ON
			JMP IGNORE

		_NOTE_OFF:
			CALL NOTE_OFF

		IGNORE:

		DELAY:
			CALL GET_CURRENT_TIME
			CMP eax, [elapsed]
			JBE DELAY ;JBE - Jump if Below or Equal
			MOV [elapsed], eax
			INC si ;INC - increment a byte or word by 1
			CMP si, [song_size]
			JB NEXT_NOTE ;JB - Jump if Below

		END:
			RET ;RET - Return to the line where this is called

		CALL STOP_CLOCK
		CALL END_P

	END_P: ;label to close the program and return to DOS
		MOV ax, 4c00h
		INT 21h

	CLEAR_SCREEN: ;label to clear the screen
		MOV ah, 0fh
		INT 10h
		MOV ah, 0
		INT 10h
		RET

	PRINT_NEWLINE: ;label to print newline (\n)
		MOV dx, OFFSET str_newline
		CALL PRINT
		RET

	PRINT: ;label to print
		MOV ah, 09h
		INT 21h
		RET

	PRINT_COLORED: ;label to print colored text
		INT 10h
		RET

	NOTE_ON:
		MOV dx, ax
		MOV al, 0b6h ; = count(1193180)/sampling rate(25Hz) = 47727
		OUT 43h, al ;Output - Used to send out a byte or word from the accumulator to the provided port.
		MOV ax, dx
		OUT 42h, al
		MOV al, ah
		OUT 42h, al
		IN al, 61h ;Input - Used to read a byte or word from the provided port to the accumulator
		OR al, 3h ; Or - Used to multiply each bit in a byte/word with the corresponding bit in another byte/word.
		OUT 61h, al
		RET

	NOTE_OFF:
		IN al, 61h
		AND al, 0fch ;And - Used for adding each bit in a byte/word with the corresponding bit in another byte/word.
		OUT 61h, al
		RET

	START_CLOCK: ;label to start the clock
		CLI ;Clear Interrup - Used to clear the interrupt enable flag to 0
		MOV al, 36h
		OUT 43h, al
		MOV al, 6fh ; low
		OUT 40h, al
		MOV al, 0bah ; high
		OUT 40h, al
		STI ;Set Interrupt - Used to set the interrupt enable flag to 1
		RET

	STOP_CLOCK: ;label to stop the clock
		CLI
		MOV al, 36h
		OUT 43h, al
		MOV al, 0h ; low
		OUT 40h, al
		MOV Al, 0h ; high
		OUT 40h, al
		STI
		RET

	GET_CURRENT_TIME:
		PUSH es ;Used to put a word at the top of the stack.
		MOV ax, 0
		MOV es, ax
		MOV eax, [es:46ch]
		POP es ;Used to get a word from the top of the stack to the provided location.
		RET

MAIN ENDP

